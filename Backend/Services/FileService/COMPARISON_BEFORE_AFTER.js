// ПОРІВНЯННЯ ПО ЯКОСТЯХ
// ============================================================

function CalculateGlobalProgress(completedQualities, currentQualityProgress, totalQualities) {
    const overallProgressRaw = (completedQualities + currentQualityProgress / 100.0) / totalQualities * 100;
    const overallProgress = 10 + Math.round(overallProgressRaw * 0.8);
    return Math.max(10, Math.min(90, overallProgress));
}

